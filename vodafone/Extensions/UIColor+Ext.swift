import Foundation
import UIKit

extension UIColor {
    class func darkPurple() -> UIColor {
        return UIColor(red: 70/255, green: 67/255, blue: 126/255, alpha: 1.0)
    }
    class func vodafoneRed() -> UIColor {
        return UIColor(red: 230/255, green: 0/255, blue: 0/255, alpha: 1.0)
    }
}
